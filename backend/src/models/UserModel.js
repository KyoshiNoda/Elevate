class User {
  user_id = 0;
  name = "";
  email = "";
  password = "";
  height = "";
  weight = "";
  constructor(user_id, email, password) {
    this.user_id = user_id;
    this.name = name;
    this.email = email;
    this.password = password;
    this.height = this.height;
    this.weight = this.weight;
  }
}
module.exports = User;
